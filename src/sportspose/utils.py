def chunks(lst, n, m=0):
    """Yield successive n-sized chunks from lst.
    Args:
        lst (list): List to be split into chunks.
        n (int): Group size
        m (int): Overlap size
    """
    for i in range(0, len(lst), n - m):
        yield lst[i : i + n]


